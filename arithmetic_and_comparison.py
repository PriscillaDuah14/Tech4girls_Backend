#python
# Define two variables
x = 10
y = 3

# Perform calculations
addition = x + y
subtraction = x - y
multiplication = x * y
division = x / y
floor_division = x // y
exponentiation = x ** y
modulus = x % y

# Compare x and y
equal = x == y
not_equal = x != y
less_than = x < y
greater_than = x > y
less_than_or_equal = x <= y
greater_than_or_equal = x >= y

# Print results using f-strings
print(f"Addition (x + y): {addition}")
print(f"Subtraction (x - y): {subtraction}")
print(f"Multiplication (x * y): {multiplication}")
print(f"Division (x / y): {division}")
print(f"Floor Division (x // y): {floor_division}")
print(f"Exponentiation (x ** y): {exponentiation}")
print(f"Modulus (x % y): {modulus}")

print(f"x == y: {equal}")
print(f"x != y: {not_equal}")
print(f"x < y: {less_than}")
print(f"x > y: {greater_than}")
print(f"x <= y: {less_than_or_equal}")
print(f"x >= y: {greater_than_or_equal}")
