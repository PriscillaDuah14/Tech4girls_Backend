# Define a tuple with 5 different elements
my_tuple = (1, "apple", 3.14, True, "banana")

# Print the first element (index 0)
first_element = my_tuple[0]
print("First element:", first_element)

# Print the last element (index -1)
last_element = my_tuple[-1]
print("Last element:", last_element)


# Define a tuple with 5 different elements
my_tuple = (1, "apple", 3.14, True, "banana", "apple")

# Use count() to find the number of occurrences of "apple"
apple_count = my_tuple.count("apple")
print("Count of 'apple':", apple_count)

# Use index() to find the index of the first occurrence of "apple"
apple_index = my_tuple.index("apple")
print("Index of first 'apple':", apple_index)


